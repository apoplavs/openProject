    def validate_text(self, text):
        """
        валідує текст
        :param text: str
        :return: str
        """
        if not (self.part == 'full'):
            text = self.cut_part(text)

        # якщо не вдалось обрізати потрібну частину тексту  
        if text == None:
              return None

        # додаємо пробіли з початку і в кінці тексту
        text = ' ' + text + ' '

        # виставлення всіх слів через один пробіл в тому числі замість \n
        text = re.sub('\s+', ' ', text)
        # видалення цифр
        text = re.sub(r"\d+", " ", text)
        # видалення службових слів(ОСОБА_1 ІНФОРМАЦІЯ_1)
        reg_e = re.compile(r"ОСОБ\w+,?|ІНФОРМА\w+,?|АДРЕС\w+,?|НОМЕ\w+,?",
                           re.UNICODE)
        text = re.sub(reg_e, "", text)

        # приведення до нижнього регістру
        text = text.lower()

        # виставляємо коректно пунктуацію
        text = self._set_punctuation(text)

        # видаляємо короткі слова і сполучники (2 рази)
        reg_e = re.compile(r" .{1,3} ", re.UNICODE)
        text = re.sub(reg_e, " ", text)
        text = re.sub(reg_e, " ", text)

        # видалення непотрібних слів
        text = self._del_needless_words(text)

        # обрізаємо пробіли з початку і кінця речення
        text = text.strip()

        # якщо текст не закінчується крапкою, доставляємо її
        if not text[-1:] == '.':
            text = text + '.'

        # повторне виставлення всіх слів через один пробіл
        text = re.sub(' +', ' ', text)

        return text

    def _set_punctuation(self, text):
        """
        виставляє пунктуацію в тексті, відповідно до правил Української мови
        видаляє непотрібну пунктуацію
        :param text: str
        :return: str
        """
        # між коротким відрізками тексту видаляємо знаки пунктуації
        text = re.sub(r"[\.,](.){1,14}[\.,]", r".\1", text)

        # виставляємо коректно крапку
        text = re.sub(r"(\w)\.(\w)", r"\1. \1", text)
        text = re.sub(r"(\.,|,\.|\.\.|,)", ".", text)

        # видаляємо всі знаки пунктуації крім крапки
        text = re.sub(r"[^\w. ]+", r"", text)

        return text

